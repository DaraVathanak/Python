
a = 5
b = 10

print("a == b:", a == b)  # False

print("a != b:", a != b)  # True

print("a > b:", a > b)    # False

print("a < b:", a < b)    # True

print("a >= b:", a >= b)  # False

print("a <= b:", a <= b)  # True

if a < b:
    print(f"{a} is less than {b}")
else:
    print(f"{a} is not less than {b}")
