a = 60  # 0011 1100
b = 13  # 0000 1101

print("a & b =", a & b)     # 12  (0000 1100)
print("a | b =", a | b)     # 61  (0011 1101)
print("a ^ b =", a ^ b)     # 49  (0011 0001)
print("~a =", ~a)           # -61 (in 2's complement)

print("a << 2 =", a << 2)   # 240 (1111 0000)
print("a >> 2 =", a >> 2)   # 15  (0000 1111)
