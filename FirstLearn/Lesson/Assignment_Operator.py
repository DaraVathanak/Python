a = 10
print("Initial value of a:", a)

a += 5
print("After a += 5:", a)   # 15

a -= 3
print("After a -= 3:", a)   # 12

a *= 2
print("After a *= 2:", a)   # 24

a /= 4
print("After a /= 4:", a)   # 6.0

a %= 5
print("After a %= 5:", a)   # 1.0

a = 3
a **= 2
print("After a **= 2:", a)  # 9

a = 9
a //= 2
print("After a //= 2:", a)  # 4
